export function resolveLocale(value?: string | null): Locale {
  const normalized = value?.trim().toLowerCase().replace("_", "-")

  if (!normalized) {
    return DEFAULT_LOCALE
  }

  if (normalized === "zh-tw" || normalized === "zh-hk" || normalized === "zh-mo") {
    return "zh-TW"
  }

  if (normalized.startsWith("zh")) return "zh"
  if (normalized.startsWith("ja")) return "ja"
  if (normalized.startsWith("ko")) return "ko"
  if (normalized.startsWith("es")) return "es"
  if (normalized.startsWith("fr")) return "fr"
  if (normalized.startsWith("de")) return "de"
  if (normalized.startsWith("pt")) return "pt"

  return "en"
}

export function resolveLocaleFrom(...values: Array<string | null | undefined>): Locale {
  for (const value of values) {
    if (typeof value === "string" && value.trim()) {
      return resolveLocale(value)
    }
  }

  return DEFAULT_LOCALE
}

export function getDocumentLang(locale: Locale) {
  const map: Record<Locale, string> = {
    en: "en",
    zh: "zh-CN",
    "zh-TW": "zh-TW",
    ja: "ja",
    ko: "ko",
    es: "es",
    fr: "fr",
    de: "de",
    pt: "pt",
  }

  return map[locale]
}

export function isCjkLocale(locale: Locale) {
  return locale === "zh" || locale === "zh-TW" || locale === "ja" || locale === "ko"
}

export function t(locale: Locale, key: MessageKey, values?: Record<string, string | number>) {
  const template = String(studioMessages[locale][key])

  if (!values) {
    return template
  }

  return Object.entries(values).reduce<string>(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    template
  )
}

export function pluralSuffix(locale: Locale, count: number) {
  if (isCjkLocale(locale)) {
    return ""
  }

  return count === 1 ? "" : "s"
}
